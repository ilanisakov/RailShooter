#include "Projectile.h"

/////////////////////////////////////////////////////////////////////
// Constructor
/////////////////////////////////////////////////////////////////////
Projectile::Projectile(PROJECTILE_TYPE type, String name): MyEntityClass(name)
{
	this->projType = type;
}

/////////////////////////////////////////////////////////////////
// Render()
/////////////////////////////////////////////////////////////////
void Projectile::Update()
{
	if (!m_bAlive)
		return;
	//Else alive

	//basic pokecube
	//check killzone?
	vector3 pos = GetPosition();
	//printf("PC:ps x[%f] y[%f] z[%f]\n", pos[0], pos[1], pos[2]);
	if (pos[1] < -30.0f)
	{
		m_bAlive = false;
	}
	else
	{
		//move
		MyEntityClass::Update();
	}

}

/////////////////////////////////////////////////////////////////////
// SetAlive() - set wether projectile is alive
/////////////////////////////////////////////////////////////////////
void Projectile::SetAlive(bool b)
{
	m_bAlive = b;
}
/////////////////////////////////////////////////////////////////////
// IsAlive() - return wether projectile is alive
/////////////////////////////////////////////////////////////////////
bool Projectile::IsAlive()
{
	return m_bAlive;
}